export const PUNCTUATION = [".", ",", "!", "?", ";", ":"];

export type TypingMode = 'words' | 'sentences' | 'paragraphs';
export type Difficulty = 'easy' | 'medium' | 'hard';

export const generateWords = (count: number, includeNumbers: boolean, includePunctuation: boolean, difficulty: Difficulty) => {
    let sourceWords = COMMON_WORDS;
    if (difficulty === 'easy') sourceWords = WORDS_EASY;
    if (difficulty === 'hard') sourceWords = WORDS_HARD;

    const words = [];
    for (let i = 0; i < count; i++) {
        let word = sourceWords[Math.floor(Math.random() * sourceWords.length)];

        if (includeNumbers && Math.random() < 0.1) {
            word = Math.floor(Math.random() * 1000).toString();
        }

        if (includePunctuation && Math.random() < 0.2) {
            word += PUNCTUATION[Math.floor(Math.random() * PUNCTUATION.length)];
        }
        words.push(word);
    }
    return words.join(' ');
};

export const generateSentences = (wordCountEstimate: number, difficulty: Difficulty) => {
    let text = "";
    let currentWordCount = 0;

    let sourceSentences = SENTENCES_MEDIUM;
    if (difficulty === 'easy') sourceSentences = SENTENCES_EASY;
    if (difficulty === 'hard') sourceSentences = SENTENCES_HARD;

    while (currentWordCount < wordCountEstimate) {
        const sentence = sourceSentences[Math.floor(Math.random() * sourceSentences.length)];
        text += (text ? " " : "") + sentence;
        currentWordCount += sentence.split(' ').length;
    }
    return text;
};

export const applyTextTransformations = (text: string, includeNumbers: boolean, includePunctuation: boolean) => {
    if (!includeNumbers && !includePunctuation) return text;

    return text.split(' ').map(word => {
        let newWord = word;

        // Randomly replace word with number (10% chance)
        if (includeNumbers && Math.random() < 0.1) {
            newWord = Math.floor(Math.random() * 1000).toString();
            // Preserve original punctuation if any (simple heuristic)
            if ([',', '.', '!', '?', ';'].includes(word.slice(-1))) {
                newWord += word.slice(-1);
            }
        }

        // Randomly add punctuation (15% chance) if not already punctuated
        // We only add if the word doesn't already have end punctuation
        if (includePunctuation && Math.random() < 0.15) {
            const lastChar = newWord.slice(-1);
            if (!['.', ',', '!', '?', ';', ':'].includes(lastChar)) {
                newWord += PUNCTUATION[Math.floor(Math.random() * PUNCTUATION.length)];
            }
        }

        return newWord;
    }).join(' ');
};
